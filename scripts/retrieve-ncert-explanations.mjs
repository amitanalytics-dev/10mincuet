#!/usr/bin/env node
/**
 * retrieve-ncert-explanations.mjs
 *
 * Checks all batch IDs saved by generate-ncert-explanations.mjs.
 * For each completed batch, streams results, then:
 *   1. Writes/merges app/data/ncert-explanations.ts (static TS map)
 *   2. Upserts all results into Convex via the batchUpsertExplanations mutation
 *
 * Run: node scripts/retrieve-ncert-explanations.mjs
 *
 * Safe to re-run — already-processed batches are skipped and results are merged.
 */

import Anthropic from "@anthropic-ai/sdk";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import dotenv from "dotenv";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

// ── Load .env.local ────────────────────────────────────────────────────────
const envPath = path.join(ROOT, ".env.local");
if (fs.existsSync(envPath)) {
  dotenv.config({ path: envPath });
}

if (!process.env.ANTHROPIC_API_KEY) {
  console.error("ERROR: ANTHROPIC_API_KEY not set in .env.local or environment");
  process.exit(1);
}

const CONVEX_URL = process.env.NEXT_PUBLIC_CONVEX_URL || process.env.CONVEX_URL;

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

// ── Parse ncert-mapping.ts via regex (for metadata on each result) ─────────
function parseNcertMapping() {
  const mappingPath = path.join(ROOT, "app/data/ncert-mapping.ts");
  const src = fs.readFileSync(mappingPath, "utf8");
  const map = {};

  const entryRegex = /"([^"]+)":\s*\{([^}]+)\}/g;
  let m;
  while ((m = entryRegex.exec(src)) !== null) {
    const topicName = m[1];
    const body = m[2];
    const book = (body.match(/book:\s*"([^"]+)"/) || [])[1];
    const chapter = parseInt((body.match(/chapter:\s*(\d+)/) || [])[1] || "0");
    const chapterName = (body.match(/chapterName:\s*"([^"]+)"/) || [])[1];

    if (book) {
      map[topicName] = { book, chapter, chapterName };
    }
  }
  return map;
}

// ── Slug → readable name (reverse-slugify best-effort) ────────────────────
function unslugify(slug) {
  return slug
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

// ── Derive topic name from the key's topic portion ─────────────────────────
// The custom_id format is "topicSlug__subconceptSlug"
// We look it up against a slug→name map built from topics.ts
function buildSlugToNameMap() {
  const topicsPath = path.join(ROOT, "app/data/topics.ts");
  const src = fs.readFileSync(topicsPath, "utf8");

  const slugMap = {};

  // Extract topic names
  const topicNameRegex = /name:\s*"([^"]+)",\s*\n\s*avgQuestionsPerPaper/g;
  let m;
  while ((m = topicNameRegex.exec(src)) !== null) {
    const name = m[1];
    const slug = name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
    slugMap[slug] = name;
  }

  // Extract subConcept names
  const scRegex = /name:\s*"([^"]+)",\s*tip:/g;
  while ((m = scRegex.exec(src)) !== null) {
    const name = m[1];
    const slug = name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
    slugMap[slug] = name;
  }

  return slugMap;
}

// ── Load existing explanations from file (if any) ─────────────────────────
function loadExistingExplanations() {
  const filePath = path.join(ROOT, "app/data/ncert-explanations.ts");
  if (!fs.existsSync(filePath)) return {};

  const src = fs.readFileSync(filePath, "utf8");
  const map = {};

  // Parse lines like:   "key": "value",
  const lineRegex = /^\s*"([^"]+)":\s*`([\s\S]*?)`,?\s*$/gm;
  let m;
  while ((m = lineRegex.exec(src)) !== null) {
    map[m[1]] = m[2];
  }

  // Also try double-quote values (fallback)
  if (Object.keys(map).length === 0) {
    const dqRegex = /^\s*"([^"]+)":\s*"((?:[^"\\]|\\.)*)"/gm;
    while ((m = dqRegex.exec(src)) !== null) {
      map[m[1]] = m[2].replace(/\\n/g, "\n").replace(/\\"/g, '"');
    }
  }

  return map;
}

// ── Write explanations to app/data/ncert-explanations.ts ──────────────────
function writeExplanationsFile(explanationsMap) {
  const filePath = path.join(ROOT, "app/data/ncert-explanations.ts");

  const entries = Object.entries(explanationsMap)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([key, value]) => {
      // Use template literals so explanation text can contain quotes freely
      const safeValue = value.replace(/`/g, "'").replace(/\${/g, "\\${");
      return `  "${key}": \`${safeValue}\`,`;
    })
    .join("\n");

  const content =
    `// Auto-generated by scripts/retrieve-ncert-explanations.mjs\n` +
    `// Do not edit manually — re-run the retrieve script to update.\n` +
    `// Last updated: ${new Date().toISOString()}\n\n` +
    `export const NCERT_EXPLANATIONS: Record<string, string> = {\n` +
    `${entries}\n` +
    `};\n`;

  fs.writeFileSync(filePath, content, "utf8");
  console.log(`  Written ${Object.keys(explanationsMap).length} explanations to app/data/ncert-explanations.ts`);
}

// ── Upsert into Convex via HTTP ────────────────────────────────────────────
async function upsertToConvex(records) {
  if (!CONVEX_URL) {
    console.warn("  WARN: NEXT_PUBLIC_CONVEX_URL not set — skipping Convex upsert");
    return;
  }

  // Convex HTTP API endpoint for mutations
  const url = `${CONVEX_URL.replace(/\/$/, "")}/api/mutation`;

  // Convex batchUpsertExplanations mutation path
  const mutationPath = "ncertExplanations:batchUpsertExplanations";

  // Split into chunks of 50 to stay within Convex mutation size limits
  const CHUNK_SIZE = 50;
  let totalUpserted = 0;

  for (let i = 0; i < records.length; i += CHUNK_SIZE) {
    const chunk = records.slice(i, i + CHUNK_SIZE);

    const body = JSON.stringify({
      path: mutationPath,
      args: { explanations: chunk },
    });

    try {
      const res = await fetch(url, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body,
      });

      if (!res.ok) {
        const text = await res.text();
        console.error(`  Convex upsert failed (chunk ${Math.floor(i / CHUNK_SIZE) + 1}): ${res.status} ${text}`);
      } else {
        totalUpserted += chunk.length;
      }
    } catch (err) {
      console.error(`  Convex upsert error (chunk ${Math.floor(i / CHUNK_SIZE) + 1}):`, err.message);
    }
  }

  if (totalUpserted > 0) {
    console.log(`  Upserted ${totalUpserted} explanations into Convex`);
  }
}

// ── Process a single completed batch ──────────────────────────────────────
async function processBatch(batchId, slugMap, ncertMap) {
  const results = {};
  const convexRecords = [];

  console.log(`  Streaming results for batch ${batchId}...`);

  for await (const result of await client.messages.batches.results(batchId)) {
    const { custom_id, result: outcome } = result;

    if (outcome.type !== "succeeded") {
      console.warn(`  SKIP ${custom_id}: outcome type = ${outcome.type}`);
      continue;
    }

    const content = outcome.message.content[0];
    if (!content || content.type !== "text") {
      console.warn(`  SKIP ${custom_id}: unexpected content type`);
      continue;
    }

    const explanation = content.text.trim();
    results[custom_id] = explanation;

    // Build Convex record
    const [topicSlug, subSlug] = custom_id.split("__");
    const topicName = slugMap[topicSlug] || unslugify(topicSlug);
    const subConceptName = slugMap[subSlug] || unslugify(subSlug || "");

    const ncertRef = ncertMap[topicName] || null;

    convexRecords.push({
      key: custom_id,
      topicName,
      subConceptName,
      explanation,
      ...(ncertRef
        ? {
            ncertBook: ncertRef.book,
            ncertChapter: ncertRef.chapter,
            ncertChapterName: ncertRef.chapterName,
          }
        : {}),
    });
  }

  return { results, convexRecords };
}

// ── Main ────────────────────────────────────────────────────────────────────
async function main() {
  const batchIdsPath = path.join(__dirname, "batch-ids-explanations.json");

  if (!fs.existsSync(batchIdsPath)) {
    console.error("ERROR: scripts/batch-ids-explanations.json not found.");
    console.error("Run `node scripts/generate-ncert-explanations.mjs` first.");
    process.exit(1);
  }

  const batchMeta = JSON.parse(fs.readFileSync(batchIdsPath, "utf8"));
  console.log(`Found ${batchMeta.length} batch(es) to check\n`);

  const slugMap = buildSlugToNameMap();
  const ncertMap = parseNcertMapping();

  // Load existing explanations to merge into
  let allExplanations = loadExistingExplanations();
  const existingCount = Object.keys(allExplanations).length;
  if (existingCount > 0) {
    console.log(`Loaded ${existingCount} existing explanations from ncert-explanations.ts`);
  }

  let anyPending = false;
  let newCount = 0;

  for (const meta of batchMeta) {
    const { batchId, batchIndex } = meta;
    console.log(`\nChecking batch ${batchIndex} (${batchId})...`);

    let batchInfo;
    try {
      batchInfo = await client.messages.batches.retrieve(batchId);
    } catch (err) {
      console.error(`  ERROR retrieving batch ${batchId}:`, err.message);
      continue;
    }

    console.log(`  Status: ${batchInfo.processing_status}`);

    if (batchInfo.processing_status !== "ended") {
      console.log(`  Not ready yet — come back later.`);
      anyPending = true;
      continue;
    }

    // Batch is done — stream results
    const { results, convexRecords } = await processBatch(batchId, slugMap, ncertMap);
    const batchCount = Object.keys(results).length;
    console.log(`  Got ${batchCount} explanations from this batch`);

    // Merge into master map
    Object.assign(allExplanations, results);
    newCount += batchCount;

    // Upsert into Convex
    await upsertToConvex(convexRecords);
  }

  if (Object.keys(allExplanations).length > 0) {
    writeExplanationsFile(allExplanations);
  }

  console.log(`\n--- Summary ---`);
  console.log(`Total explanations in file: ${Object.keys(allExplanations).length}`);
  console.log(`New explanations this run:  ${newCount}`);

  if (anyPending) {
    console.log("\nSome batches are still processing. Re-run this script in a few hours.");
  } else {
    console.log("\nAll batches processed successfully.");
  }
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
